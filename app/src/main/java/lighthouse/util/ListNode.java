package lighthouse.util;

/**
 * ListNode is an organization class that represents a node in a linked list
 */
public class ListNode {
	private ListNode next;

	public ListNode() {
		next = null;
	}

	public ListNode getNext() {
		return next;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}
}
